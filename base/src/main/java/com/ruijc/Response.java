/**
 * 操作统一返回
 *
 * @author Storezhang
 * @create 2016-12-29 02:39
 * @email storezhang@gmail.com
 * @qq 160290688
 */
public class Response<T> extends BaseObject {

    public static final int OK = 200;//操作正确
    public static final int FAILD = 400;//操作有错误
    public static final int ERROR = 500;//异常
    public static final int DISABLED = 700;//该功能已经被禁用

    private int code;//操作的状态码
    private String msg;//消息
    private T data;//数据

    public Response() {
        code = OK;
    }

    public boolean isSuccess() {
        return OK == code;
    }

    public void setSuccess(boolean success) {
        if (success) {
            code = OK;
        } else {
            code = FAILD;
        }
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }
}
